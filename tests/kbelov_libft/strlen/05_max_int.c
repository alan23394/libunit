#include "libft.h"

int max_int(void)
{
	char test[2147483647];
	
	test[2147483646] = '\0';
	while (*test != '\0')
	{
		*test = 'a';
		test++;
	}
	if (ft_strlen(test) == strlen(test))
		return(0);
	else
		return(-1);
}
